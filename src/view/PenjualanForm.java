    public boolean tambahPenjualan(Penjualan p) {
        String sql = "INSERT INTO penjualan (kode_pelanggan, kode_barang, jumlah, harga_satuan, total_harga, tanggal) VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP)";

        try (Connection conn = koneksidatabase.getConnection();
             PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setString(1, p.getKodePelanggan());
            pst.setString(2, p.getKodeBarang());
            pst.setInt(3, p.getJumlah());
            pst.setDouble(4, p.getHargaSatuan());
            pst.setDouble(5, p.getTotalHarga());

            return pst.executeUpdate() > 0;

        } catch (SQLException e) {
            System.err.println("Gagal Tambah Penjualan: " + e.getMessage());
            return false;
        }
    }

    public List<Penjualan> getAllPenjualan() {
        List<Penjualan> list = new ArrayList<>();
        String sql = "SELECT * FROM penjualan";

        try (Connection conn = koneksidatabase.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                list.add(new Penjualan(
                    rs.getInt("id_penjualan"),
                    rs.getString("kode_pelanggan"),
                    rs.getString("kode_barang"),
                    rs.getInt("jumlah"),
                    rs.getDouble("harga_satuan"),
                    rs.getDouble("total_harga"),
                    rs.getTimestamp("tanggal")
                ));
            }

        } catch (SQLException e) {
            System.err.println("Gagal Mengambil Data Penjualan: " + e.getMessage());
        }

        return list;
    }

    private void loadDataToTable() {
        List<Penjualan> list = getAllPenjualan();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Kode Pelanggan");
        model.addColumn("Kode Barang");
        model.addColumn("Jumlah");
        model.addColumn("Harga Satuan");
        model.addColumn("Total Harga");
        model.addColumn("Tanggal");

        for (Penjualan p : list) {
            model.addRow(new Object[] {
                p.getIdPenjualan(), p.getKodePelanggan(), p.getKodeBarang(),
                p.getJumlah(), p.getHargaSatuan(), p.getTotalHarga(), p.getTanggal()
            });
        }

        tabelPenjualan.setModel(model);
        setColumnWidths();
    }

